package ceneax.app.hongmiao.util;

import android.util.Log;

public class L {

    /**
     * 调试输出d
     * @param tag 标签
     * @param msg 输出的内容
     */
    public static void d(String tag, String msg) {
        Log.d(tag, msg);
    }

    /**
     * 调试输出e
     * @param tag 标签
     * @param msg 输出的内容
     */
    public static void e(String tag, String msg) {
        Log.e(tag, msg);
    }

}